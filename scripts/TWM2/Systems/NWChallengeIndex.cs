   if(%xp >= $Ranks::MinPoints[13]) {
      messageClient( %client, 'SetLineHud', "", %tag, %index, "<a:gamelink\tOtherTasksSub\t1>Killstreak Challenges</a>: Tasks for calling in advanced support");
